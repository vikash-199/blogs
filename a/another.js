// Bus stops in forward journey
const busStops = [
  "Stop 1: Central Station",
  "Stop 2: City Mall",
  "Stop 3: University",
  "Stop 4: Airport",
  "Stop 5: Beach",
];

// Reverse journey
const returnJourney = [...busStops].reverse();

console.log("Forward Journey:");
console.log(busStops);

console.log("\nReturn Journey:");
console.log(returnJourney);
